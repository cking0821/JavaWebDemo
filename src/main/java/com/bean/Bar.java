package com.bean;

public class Bar {
    /**name :服装名称
     * num: 对应服装的件数
     */
    private String name;
    private int num;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}